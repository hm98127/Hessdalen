package org.mdoubleh.www.board.event.vo;

public class EvBoardVo {
	private int ev_sq;
	private String sj;
	private String cn;
	private int hit;
	private String dttm;
	private String id;

	public int getEv_sq() {
		return ev_sq;
	}

	public void setEv_sq(int ev_sq) {
		this.ev_sq = ev_sq;
	}

	public String getSj() {
		return sj;
	}

	public void setSj(String sj) {
		this.sj = sj;
	}

	public String getCn() {
		return cn;
	}

	public void setCn(String cn) {
		this.cn = cn;
	}

	public int getHit() {
		return hit;
	}

	public void setHit(int hit) {
		this.hit = hit;
	}

	public String getDttm() {
		return dttm;
	}

	public void setDttm(String dttm) {
		this.dttm = dttm;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
