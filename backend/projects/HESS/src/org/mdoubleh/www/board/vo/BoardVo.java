package org.mdoubleh.www.board.vo;

public class BoardVo {
	private int db_sq;
	private int mb_sq;
	private String sj;
	private String cn;
	private int hit;
	private String dttm;
	private String id;

	public int getDb_sq() {
		return db_sq;
	}

	public void setDb_sq(int db_sq) {
		this.db_sq = db_sq;
	}

	public int getMb_sq() {
		return mb_sq;
	}

	public void setMb_sq(int mb_sq) {
		this.mb_sq = mb_sq;
	}

	public String getSj() {
		return sj;
	}

	public void setSj(String sj) {
		this.sj = sj;
	}

	public String getCn() {
		return cn;
	}

	public void setCn(String cn) {
		this.cn = cn;
	}

	public int getHit() {
		return hit;
	}

	public void setHit(int hit) {
		this.hit = hit;
	}

	public String getDttm() {
		return dttm;
	}

	public void setDttm(String dttm) {
		this.dttm = dttm;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
