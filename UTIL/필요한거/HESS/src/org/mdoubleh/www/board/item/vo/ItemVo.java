package org.mdoubleh.www.board.item.vo;

public class ItemVo {
	private int item_sq;
	private int img_sq;
	private int price;
	private String nm;
	private String cont;
	private String id;
	private String item_img;
	private String kind;

	public int getItem_sq() {
		return item_sq;
	}

	public void setItem_sq(int item_sq) {
		this.item_sq = item_sq;
	}

	public int getImg_sq() {
		return img_sq;
	}

	public void setImg_sq(int img_sq) {
		this.img_sq = img_sq;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getNm() {
		return nm;
	}

	public void setNm(String nm) {
		this.nm = nm;
	}

	public String getCont() {
		return cont;
	}

	public void setCont(String cont) {
		this.cont = cont;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getItem_img() {
		return item_img;
	}

	public void setItem_img(String item_img) {
		this.item_img = item_img;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}

}
